    def _compute_ring_indices(self):
        """Compute ring index mapping based on circle radii."""
        radii = [round(c.radius, 6) for c in self.circles]
        unique_radii = sorted(set(radii))
        return {r: i for i, r in enumerate(unique_radii)}
    
